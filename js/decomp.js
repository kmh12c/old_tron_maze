
function myExplode(delimstring, breakstring)
{
  var ret = new Array();
  var prev = -1;
  for(i=0; i<=breakstring.length-delimstring.length; i++)
  {
    var c = 0;
    if(php_substr(breakstring,i,1)=="{")
    {
      c++;
      i++;
      while(c!=0)
      {
        if(php_substr(breakstring,i,1)=="{") c++;
        else if(php_substr(breakstring,i,1)=="}") c--;
        i++;
      }
    }
    if(php_substr(breakstring,i,1)=="\"")
      { i++; while(php_substr(breakstring,i,1)!="\"" && i<breakstring.length)i++;}
    if(php_substr(breakstring,i,delimstring.length)==delimstring)
    {
      ret.push(php_substr(breakstring,prev+1,i-prev-1));
      prev = i;
    }
  }
  ret.push(php_substr(breakstring,prev+1));
  return ret;
}

function getVar(varstring)
{
  if (typeof varstring === 'undefined') {
    varstring = "";
  }
  if(php_substr(varstring,0,1)=="{")
  {
    var ret = new Array();
    varstring = php_substr(varstring,1,-1);
    if(varstring === "") return ret;
    var split = myExplode(",",varstring);
    for(var i = 0; i < count(split); i++)
    {
      var bum = myExplode(":",split[i]);
      ret[getVar(bum[0])] = getVar(bum[1]);
    }
    return ret;
  }
  else if(php_substr(varstring,0,1)=="\"")
    return php_substr(varstring,1,-1);
  else return varstring;
}

function decompose(filestring)
{
  filestring = trim(filestring);
  var vars = new Array();
  if(filestring == "") return vars;
  var spiffyarray = myExplode(",",filestring);
  var i=0;
  for(i = 0; i < count(spiffyarray); i++)
  {
    var bum = myExplode(":",spiffyarray[i]);
    vars[getVar(bum[0])] = getVar(bum[1]);
  }
  return vars;
}


